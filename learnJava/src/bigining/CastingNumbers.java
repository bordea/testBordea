package bigining;

public class CastingNumbers {
    public static void main(String[] args) {
        double above = 0.7, below = 0.4;
        float fabove = 0.7f, fbelow = 0.4f;
        System.out.println(above + "(int)above: " + (int)above);
        System.out.println(below + "(int)below: " + (int)below);
        System.out.println(fabove + "(int)fabove: " + (int)fabove);
        System.out.println(fbelow + "(int)fbelow: " + (int)fbelow);
    }
}